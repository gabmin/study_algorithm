#     북 동 남 서
dr = [-1, 0, 1, 0]
dc = [0, 1, 0, -1]

def rotate_to_left(d):
    return (d + 3) % 4

def go_back(d):
    return (d + 2) % 4

def get_count_of_departments_cleaned_by_robot_vacuum(r, c, d, room_map):
    n = len(room_map)
    m = len(room_map[0])

    result = 1
    room_map[r][c] = 2
    queue = deque([[r, c, d]])

    while queue:
        r, c, d = queue.popleft()
        temp_d = d

        for i in range(4):
            temp_d = rotate_to_left(temp_d)
            new_r, new_c = r + dr[temp_d], c + dc[temp_d]

            if 0 <= new_r < n and 0 <= new_c < m and room_map[new_r][new_c] == 0:
                result += 1
                room_map[new_r][new_c] = 2
                queue.append([new_r, new_c, temp_d])
                break
            elif i == 3:
                temp_d = go_back(d)
                new_r, new_c = r + dr[temp_d], c + dc[temp_d]
                queue.append([new_r, new_c, d])

                if room_map[new_r][new_c] == 1:
                    return result

